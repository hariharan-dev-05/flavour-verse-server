import { model, Schema } from "mongoose";

const userSchema = new Schema({
        username: {
            type: String,
            required: true,
            trim: true
        },
        email: {
            type: String,
            unique: true,
            required: true,
            trim: true
        },
        password: {
            type: String,
            required: true
        },
        role: {
            type: String,
            enum: ["user", "admin"],
            default: "user"
        },
    }
)

const User = model("User", userSchema);

export default User;